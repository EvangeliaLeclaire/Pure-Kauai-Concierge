export type PricingModel =
  | "per_person"    // price × (adults + children)
  | "per_adult"     // price × adults only
  | "per_child"     // price × children only
  | "per_night"     // price × numNights
  | "per_couple"    // price × ceil(adults / 2)
  | "per_event"     // flat rate, no multiplication
  | "complimentary"; // $0

